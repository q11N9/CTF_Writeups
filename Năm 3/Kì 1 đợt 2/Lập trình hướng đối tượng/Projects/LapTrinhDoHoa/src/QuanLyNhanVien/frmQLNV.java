    private void btntongLuongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntongLuongActionPerformed
        // TODO add your handling code here:
        try {
            
            List<NhanVien> danhSachNhanVien = XuLyFile.readFromFile();

            
            double tongLuong = 0;
            for (NhanVien nv : danhSachNhanVien) {
                tongLuong += nv.getLuong();
            }

            tongLuongTextBox.setText(String.format("%.2f", tongLuong));
        } catch (IOException ex) {
          
            JOptionPane.showMessageDialog(this, "Lỗi đọc file: " + ex.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btntongLuongActionPerformed
    
