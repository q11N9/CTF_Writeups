    private void btnluuFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuFileActionPerformed
        // TODO add your handling code here:
        try {
            String maNV = maNVTextBox.getText();
            String hoTen = hoTenTextBox.getText();
            String dienThoai = sdtTextBox.getText();
            double heSoLuong = Double.parseDouble(hslTextBox.getText());
            
            if (maNV.isEmpty() || hoTen.isEmpty() || dienThoai.isEmpty()){
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin", 
                        "Lỗi nhập liệu", JOptionPane.WARNING_MESSAGE);
            }
            NhanVien nv = new NhanVien(maNV, hoTen, dienThoai, heSoLuong);
            double luong = nv.getLuong();
            luongTextBox.setText(String.format("%.2f", luong));
            XuLyFile.saveToFile(nv);
            JOptionPane.showMessageDialog(this, "Nhân viên đã được thêm thành công!");
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Hệ số lương phải là số!", "Lỗi nhập liệu", JOptionPane.ERROR_MESSAGE);
            
        }catch (IOException e){
            JOptionPane.showMessageDialog(this, "Lỗi ghi file!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnluuFileActionPerformed

    private void btndocFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndocFileActionPerformed
        // TODO add your handling code here:
        try{
            List<NhanVien> dsNhanVien = XuLyFile.readFromFile();
            if (dsNhanVien.isEmpty()){
                hienThiFile.setText("Không có nhân viên nào trong file!");
                return;
            }
            StringBuilder builder = new StringBuilder();
            for (NhanVien nv : dsNhanVien){
                builder.append(nv.toString()).append("\n");
            }
            hienThiFile.setText(builder.toString());
            
            DefaultTableModel model = (DefaultTableModel) tbDSNhanVien.getModel();
            model.setRowCount(0);
            for(NhanVien nv : dsNhanVien){
                Object[] rowData = {
                    nv.getMaNV(),
                    nv.getHoTen(),
                    nv.getDienThoai(),
                    nv.getHeSoLuong(), 
                    nv.getLuong()
                };
                model.addRow(rowData);
            }
            
            DefaultTableCellRenderer leftAlignRenderer = new DefaultTableCellRenderer();
            leftAlignRenderer.setHorizontalAlignment(SwingConstants.LEFT);
            for (int i = 0; i < tbDSNhanVien.getColumnCount(); i++){
                tbDSNhanVien.getColumnModel().getColumn(i).setCellRenderer(leftAlignRenderer);
            }
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "Lỗi khi đọc file!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btndocFileActionPerformed

