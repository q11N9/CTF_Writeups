    private void luuTruButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_luuTruButtonActionPerformed
        // TODO add your handling code here:
        // Lay du lieu tu nguoi dung
        try{
            
            int id = Integer.parseInt(idTextbox.getText());
            String hoTen = hoTenTextbox.getText();
            String maGV = maGVTextbox.getText();
            String diaChi = diaChiTextbox.getText();
            String gioiTinh = gioiTinhTextbox.getText();
            String khoa = khoaTextbox.getText();

            // tao mot doi tuong GiangVien moi
            GiangVien newGV = new GiangVien(id, hoTen, maGV, diaChi, gioiTinh, khoa);
            if(XuLyFile.ghiGVFile(newGV)){
                textArea.setText("Da ghi file thanh cong!");
            }else{
                textArea.setText("ID da ton tai!");
            }
        }catch (Exception e){
            textArea.setText("Loi: " + e.getMessage());
        }
    }//GEN-LAST:event_luuTruButtonActionPerformed

    private void timKiemButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timKiemButtonActionPerformed
        // TODO add your handling code here:
        try{
            String hoTen = hoTenTextbox.getText();
            List<GiangVien> gvInfo = XuLyFile.timGVTheoTen(hoTen);
            if (gvInfo.size() != 0){
                textArea.setText("Thong tin cua cac giang vien co ten " + hoTen + "la: \n");
                for (GiangVien gv : gvInfo){
                    textArea.append(gv.hienThi());
                }
            }
            else{
                textArea.setText("Khong tim thay giang vien!");
            }
        }catch (Exception ex){
            textArea.setText("Loi: " + ex.getMessage());
        }
    }//GEN-LAST:event_timKiemButtonActionPerformed

    private void docFileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_docFileButtonActionPerformed
        // TODO add your handling code here:
        textArea.setText("");
        List<GiangVien> gvList = XuLyFile.docFile();
        for(GiangVien gv : gvList){
            textArea.append(gv.hienThi());
        }
    }//GEN-LAST:event_docFileButtonActionPerformed

