    private void HienThiAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HienThiAActionPerformed
        // TODO add your handling code here:
        textArea.setText("");
        if (!danhsach.isEmpty()) {
            textArea.append("====> Danh sach khoi A Trung Tuyen <====\n");
            ArrayList<ThisinhkhoiA> danhsachA = new ArrayList<>();

            for (Thisinh ts : danhsach) {
                if (ts instanceof ThisinhkhoiA) {
                    danhsachA.add((ThisinhkhoiA) ts);
                }
            }

            for (ThisinhkhoiA tska : danhsachA) {
                if (tska.getDtoan() + tska.getDly() + tska.getDhoa() > 20) {
                    textArea.append(tska.toString());
                }
            }
        }
    }//GEN-LAST:event_HienThiAActionPerformed

