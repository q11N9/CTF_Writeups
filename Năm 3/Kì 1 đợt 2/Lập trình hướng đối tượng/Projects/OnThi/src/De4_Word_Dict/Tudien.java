    private void traCuuButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_traCuuButtonActionPerformed
        // TODO add your handling code here:
        String searchWord = engText.getText().trim();
        if (searchWord.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập từ cần tìm!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String result = dictionary.binarySearch(searchWord);
        if (result != null) {
            vieText.setText(result); // Hiển thị nghĩa tiếng Việt
            JOptionPane.showMessageDialog(this, "Tìm thấy từ: " + searchWord, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy từ này!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_traCuuButtonActionPerformed

    private void themButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themButtonActionPerformed
        // TODO add your handling code here:
        String enWord = engText.getText().trim();
        String vnWord = vieText.getText().trim();
        
        if (enWord.isEmpty() || vnWord.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ từ tiếng Anh và tiếng Việt!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        // Kiem tra xem da co trong tu dien chua
        
        boolean added = dictionary.addWord(enWord, vnWord);
        if (added) {
            JOptionPane.showMessageDialog(this, "Đã thêm từ mới vào từ điển!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Từ này đã tồn tại trong từ điển!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_themButtonActionPerformed

    private void suaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suaButtonActionPerformed
        // TODO add your handling code here:
        String enWord = engText.getText().trim();
        String vnWord = vieText.getText().trim();
        
        if (enWord.isEmpty() || vnWord.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ từ tiếng Anh và nghĩa mới tiếng Việt!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        boolean updated = dictionary.updateWord(enWord, vnWord);
        if (updated) {
            JOptionPane.showMessageDialog(this, "Đã cập nhật nghĩa của từ!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy từ này trong từ điển!", "Thông báo", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_suaButtonActionPerformed

