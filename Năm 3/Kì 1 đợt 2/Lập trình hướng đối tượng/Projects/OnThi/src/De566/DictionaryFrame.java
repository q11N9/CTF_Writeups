    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        
        String tiengAnh = tiengAnhText.getText();
        TuVung tuVung = XuLyFile.timTheoTiengAnh(tiengAnh);
        if (tuVung != null){
            JOptionPane.showMessageDialog(null, "Tìm thấy từ trong từ điển!", "Kết quả tìm kiếm", 
                    JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(null, "Không tìm thấy từ tương ứng trong từ điển!", 
                    "Kết quả tìm kiếm", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_searchButtonActionPerformed
    
    // Them tu moi vao tu dien
    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String tiengAnh = tiengAnhText.getText();
        String nghia = tiengVietText.getText();
        // Kiểm tra điều kiện đầu vào
        if (tiengAnh.isEmpty() || nghia.isEmpty()){
            JOptionPane.showMessageDialog(null, "Không được để trống từ tiếng Anh hoặc nghĩa!"
            , "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        // Kiểm tra trùng lặp
        if (XuLyFile.kiemTraTrungLap(tiengAnh)){
            JOptionPane.showMessageDialog(null,  "Từ \"" + tiengAnh +"\" đã tồn tại!", "Thông báo", 
                                          JOptionPane.WARNING_MESSAGE);
            return;
        }
        // Tạo ID mới cho từ vựng
        int idMoi = XuLyFile.taoID();
        TuVung tuMoi = new TuVung(idMoi, tiengAnh, nghia);
        XuLyFile.themTuMoi(tuMoi);
        JOptionPane.showMessageDialog(null, "Từ mới đã được thêm vào từ điển!", "Thông báo", 
                JOptionPane.INFORMATION_MESSAGE);  
    }//GEN-LAST:event_addButtonActionPerformed

    private void fixButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fixButtonActionPerformed
        // TODO add your handling code here:
        newEngLabel.setEnabled(true);
        newVietLabel.setEnabled(true);
        newEnglishText.setEnabled(true);
        newVietnameseText.setEnabled(true);
        String oldEng = tiengAnhText.getText();
        String newEng = newEnglishText.getText();
        String newViet = newVietnameseText.getText();
        if (oldEng.isEmpty() || newEng.isEmpty() || newViet.isEmpty()){
            JOptionPane.showMessageDialog(null, "Không được để trống ô Tiếng Anh và 2 ô mới!", "Lỗi",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        // Tim trong file xem co tu nao trung khop hay khong
        List<TuVung> tuVungList = XuLyFile.docFile();
        TuVung tuCanSua = null;
        for (TuVung tv : tuVungList){
            if(tv.getTiengAnh().equalsIgnoreCase(oldEng)){
                tuCanSua = tv;
                break;
            }
        }
        // Neu khong tim thay
        if (tuCanSua == null){
            JOptionPane.showMessageDialog(null, "Không tìm thấy \"" + oldEng + "\" trong danh sách", 
            "Kết quả tìm kiếm", JOptionPane.WARNING_MESSAGE);
            return;
        }
        // Cập nhật từ mới
        tuCanSua.setTiengAnh(newEng);
        tuCanSua.setTiengViet(newViet);
        XuLyFile.ghiFile(tuVungList);
        JOptionPane.showMessageDialog(null, "Đã sửa thành công!", "Thông báo",
                JOptionPane.INFORMATION_MESSAGE);
        // Disable cac label va textbox moi
        newEngLabel.setEnabled(false);
        newVietLabel.setEnabled(false);
        newEnglishText.setEnabled(false);
        newVietnameseText.setEnabled(false);
    }//GEN-LAST:event_fixButtonActionPerformed

    private void showButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showButtonActionPerformed
        // TODO add your handling code here:
        List<TuVung> tuVungList = XuLyFile.docFile();
        DefaultTableModel model = (DefaultTableModel)tuDienTable.getModel();
        model.setRowCount(0);
        for (TuVung tv : tuVungList){
            model.addRow(new Object[]{
                tv.getId(), tv.getTiengAnh(), tv.getTiengViet()
            });
        }
    }//GEN-LAST:event_showButtonActionPerformed
    
