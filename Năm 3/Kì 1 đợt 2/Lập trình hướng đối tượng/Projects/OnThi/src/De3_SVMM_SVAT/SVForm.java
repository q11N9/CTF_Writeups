    private void saveSVMMButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveSVMMButtonActionPerformed
        // TODO add your handling code here:
        SinhvienMatMa svmm = new SinhvienMatMa();
        svmm.setMasv(maSVText.getText());
        svmm.setHoten(hoTenText.getText());
        svmm.setNgaysinh(ngaySinhText.getText());
        svmm.setGioitinh(gioiTinhText.getText());
        svmm.setDTB(Double.parseDouble(diemTBText.getText()));
        svmm.setDiachi(diaChiText.getText());
        svmm.setDonviluong(Double.parseDouble(dvLuongText.getText()));
        dsSinhvienMM.add(svmm);
        try{
            File file = new File("svmm.dat");
            try(PrintWriter printWriter = new PrintWriter(file)){
                for (SinhvienMatMa sv : dsSinhvienMM){
                    printWriter.printf("%s-%s-%s-%s-%.2f-%s-%.2f\n", svmm.getMasv(), 
                            svmm.getHoten(), svmm.getNgaysinh(), svmm.getGioitinh(), 
                            svmm.getDTB(), svmm.getDiachi(), svmm.getDonviluong());
                }
            }        
            JOptionPane.showMessageDialog(rootPane, "Ghi file thành công!");
                   
        }catch(FileNotFoundException ex){
            
        }
    }//GEN-LAST:event_saveSVMMButtonActionPerformed

