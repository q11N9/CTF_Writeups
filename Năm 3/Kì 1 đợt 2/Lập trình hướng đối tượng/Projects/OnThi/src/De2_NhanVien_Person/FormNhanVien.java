    private void themNVButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themNVButtonActionPerformed
        // TODO add your handling code here:
        // Lấy dữ liệu nhập vào
        String hoTen = hoTenTextbox.getText();
        String ngaySinh = ngaySinhTextbox.getText();
        String diaChi = diaChiTextbox.getText();
        String gioiTinh = gioiTInhTextbox.getText();
        String phongBan = (String) phongBanCbBox.getSelectedItem();
        double heSoLuong = Double.parseDouble(heSoLuongTextbox.getText());
        double thamNien = Double.parseDouble(thamNienTextbox.getText());
        double luongCoBan = Double.parseDouble(luongCoBanTextbox.getText());
        
        
        // Tao doi tuong NhanVien de luu tru du lieu nhap vao
        NhanVien nv = new NhanVien(hoTen, ngaySinh, diaChi, gioiTinh, phongBan, heSoLuong, thamNien, luongCoBan);
        nhanVienList.add(nv);
        //Hiển thị ra table
        DefaultTableModel model = (DefaultTableModel) nvTable.getModel();
        model.addRow(new Object[]{
            nv.getHoTen(), nv.getNgaySinh(), nv.getDiaChi(), nv.getGioiTinh(),
            nv.getPhongBan(), nv.getHeSoLuong(), nv.getThamNien(), nv.getLuongCoBan(), nv.luongThucLinh()
        });
        
        // Reset text box
        hoTenTextbox.setText("");
        ngaySinhTextbox.setText("");
        diaChiTextbox.setText("");
        gioiTInhTextbox.setText("");
        phongBanCbBox.setSelectedIndex(0);
        heSoLuongTextbox.setText("");
        thamNienTextbox.setText("");
        luongCoBanTextbox.setText("");
        
    }//GEN-LAST:event_themNVButtonActionPerformed

    private void luuDataButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_luuDataButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) nvTable.getModel();
        
        // Kiểm tra xem bảng có trống hay không
        if (model.getRowCount() == 0){
            JOptionPane.showMessageDialog(this, "Không có dữ liệu để lưu", "Thông báo",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        // Luu vao file
        try{
            XuLyFile.luuFile("dienthoai.dat", nhanVienList);
            JOptionPane.showMessageDialog(this, "Lưu file thành công!", 
                    "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(this,
                    "Lỗi khi lưu dữ liệu" + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_luuDataButtonActionPerformed

