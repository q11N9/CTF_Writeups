    private void themDienThoaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themDienThoaiActionPerformed
        // TODO add your handling code here:
        // Lấy dữ liệu được nhập vào
        String maHang = maHangTB.getText();
        String ten = tenTB.getText();
        double giaBan;
            // Kiểm tra xem giá bán có phải số hay không
        try{
            giaBan = Double.parseDouble(giaBanTB.getText());
        }catch (NumberFormatException e){
            // Hiển thị cửa sổ lỗi format number ra màn hình
            JOptionPane.showMessageDialog(this, "Giá bán phải là số", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String nhaSanXuat = nhaSanXuatTB.getText();
        String dungLuong = dungLuongTB.getText();
        String mauSac = mauSacTB.getText();
        
        // Tạo một đối tượng DienThoai và lưu vào List
        DienThoai dt = new DienThoai(maHang, ten, giaBan, nhaSanXuat, dungLuong, mauSac);
        dienThoaiList.add(dt);
        // Hiển thị ra table
            // Set model cho table
        DefaultTableModel model = (DefaultTableModel) dienThoaiTB.getModel();
        model.addRow(new Object[]{
            dt.getMaHang(),
            dt.getTen(),
            dt.getGiaBan(),
            dt.getNhaSanXuat(),
            dt.getDungLuongBoNho(),
            dt.getMauSac()
            
        });
        
        // Reset các text box
        maHangTB.setText("");
        tenTB.setText("");
        giaBanTB.setText("");
        nhaSanXuatTB.setText("");
        dungLuongTB.setText("");
        mauSacTB.setText("");
        
    }//GEN-LAST:event_themDienThoaiActionPerformed
    
    private void luuDienThoaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_luuDienThoaiActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel) dienThoaiTB.getModel();
        
        // Kiểm tra xem bảng có trống hay không
        if (model.getRowCount() == 0){
            JOptionPane.showMessageDialog(this, "Không có dữ liệu để lưu", "Thông báo",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Nếu có, thực hiện lưu vào file
        try{
            XuLyFile.luuFile("dienthoai.dat", dienThoaiList);
            JOptionPane.showMessageDialog(this, "Lưu file thành công!", 
                    "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(this,
                    "Lỗi khi lưu dữ liệu" + e.getMessage(), "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_luuDienThoaiActionPerformed

